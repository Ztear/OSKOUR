#include <string.h>

int		ft_strcmp(char *s1, char *s2)
{
	int			i;
	int			j;

	i = 0;
	j = 0;
	while (s1[i] || s2[j])
	{
		if (s1[i] != s2[j])
		{
			if (s1[i] > s2[j])
				return (1);
			if (s1[i] < s2[j])
				return (-1);
		}
		if (s1[i] != '\0')
			i++;
		if (s2[j] != '\0')
			j++;
	}
	return (0);
}
